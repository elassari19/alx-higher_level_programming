#!/usr/bin/python3


def add(a, b):
    """addition func

    Args:
        a: first number
        b: second number

    Returns:
        return addition result
    """
    return (a + b)


def sub(a, b):
    """subtraction func

    Args:
        a: first number
        b: second integer

    Returns:
        return subtraction result
    """
    return (a - b)


def mul(a, b):
    """ multiplication func

    Args:
        a: first number
        b: second number

    Returns:
        return multiplication result
    """
    return (a * b)


def div(a, b):
    """ division func

    Args:
        a: first number
        b: second number

    Returns:
        return division result
    """
    return int(a / b)
